package Default;

public class Programa12 {
    public static void main(String[] args) {
        // TIPOS PRIMÁRIOS || PRIMITIVOS
        // por padrão os números reais em Java são considerados double
        float preco1 = 23.4f; //menos espaço
        double preco2 = 23.446328274823;

        // TIPOS NÃO PRIMÁRIOS || NÃO PRIMITIVOS
        Float preco3 = 34.5f;
        Double preco4 = 244.4;

        System.out.println(preco1 + " " + preco2 + " " + preco3 + " " + preco4);
    }
}