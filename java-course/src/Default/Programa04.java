// IF, ELSE, ELSE IF
package Default;

public class Programa04 {
    public static void main(String[] args) {
        int num = 10;

        if (num > 5) {
            System.out.println("O número " + num + " é maior que 5");
        } else if (num == 5) {
            System.out.println("O número " + num + " é igual a 5");
        } else {
            System.out.println("O número " + num + " é menor que 5");
        }
    }
}